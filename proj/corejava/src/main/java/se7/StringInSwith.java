public class StringInSwith {
    public static void main(String[] args) {
        String s="ravi";
        switch(s)
        {
            case "ram":
                System.out.println("ram");
                break;
            case "raj":
                System.out.println("raj");
                break;
            case "ravi":
                System.out.println("ravi");
                break;
            default:
                System.out.println("default");

        }
    }
}
